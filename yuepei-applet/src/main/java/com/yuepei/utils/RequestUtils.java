@Component
public class RequestUtils {

    private HashMap hashMap = new HashMap<>();

    public HashMap requestParams(HttpServletRequest request){

        ServletInputStream inputStream = null;
        try {
            inputStream = request.getInputStream();

            StringBuffer stringBuffer = new StringBuffer();
            BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(inputStream));

            String bufferStr;

            while ((bufferStr = bufferedReader.readLine()) != null){
                stringBuffer.append(bufferStr);
            }

            Map parse = (Map) JSON.parse(stringBuffer.toString());

            hashMap.put("resource",JSON.parse(String.valueOf(parse.get("resource"))));
            hashMap.put("stringBuffer",stringBuffer.toString());

        } catch (IOException e) {
            e.printStackTrace();
        }
        return hashMap;
    }
}
